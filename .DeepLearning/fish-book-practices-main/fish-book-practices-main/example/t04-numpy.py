import numpy as np

A = np.array([[1, 2], [3, 4]])
print(f"A:\n{A}")
print(f"A.shape: {A.shape}\n")

B = np.array([[5, 6], [7, 8]])
print(f"B:\n{B}")
print(f"B.shape: {B.shape}\n")

print(f"A * B:\n{A * B}\n")
print(f"A.dot(B):\n{A.dot(B)}\n")


C = np.array([1, 2, 3])
print(f"C:\n{C}")
